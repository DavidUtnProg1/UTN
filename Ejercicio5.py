# Si un lote de terreno tiene X metros de frente por Y metros de fondo: calcular e imprimir la cantidad da metros de alambre para cercarlo.
x=int(input("Ingrese la cantidad de metros de frente del lote: "))
y=int(input("ingrese la cantidad de metros de fondo del lote: "))
print("la cantidad de alambre en metros que se neceistan para cercar el lote es de:", x*y,"metros")